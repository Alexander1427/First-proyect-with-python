animales = "LEon de selva"
#esta funcion hace que todo se pase a mayusculas
print(animales.upper())
#esta otra funcion hace que todo sea en minuscula
print(animales.lower())
#hace que la primera letra sea en mayuscula y las demas en minuscula
print(animales.strip().capitalize())
#hace que cada palabra pase a ser una inicial
print(animales.title())
#hace que borre los espacios en blanco del inicio y final 
print(animales.strip())
#esto sirve para reeemplazar una cadena de letras por otra
print(animales.replace("n", "pardo"))